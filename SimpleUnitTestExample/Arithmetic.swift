import Foundation

class Arithmetic {
    
    func addition(num1: Int, num2: Int) -> Int {
        let result = num1 + num2
        return result
    }
    
    func subtractration(num1: Int, num2: Int) -> Int {
        let result = num1 - num2
        return result
    }
    
    func multiplication(num1: Int, num2: Int) -> Int {
        let result = num1 * num2
        return result
    }
    
    func division(num1: Int, num2: Int) -> Int {
        let result = num1 / num2
        return result
    }
}
